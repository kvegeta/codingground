public class Animale
{
    private String nome;
    private String sesso;
    
    public Animale(String nome, String sesso)
    {
        this.nome = nome;
        this. sesso = sesso;
    }
    
    public String getNome()
    {
        return nome;    
    }

    public String getSesso()
    {
        return sesso;
    }
}